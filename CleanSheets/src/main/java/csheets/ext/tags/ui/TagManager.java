	@Override
	public void update(Observable o, Object arg) {
		this.model.removeAllElements();
		for (String task : this.contact.tags()) {
			this.model.addElement(task);
		}
		this.jListTasks.setModel(model);
		this.jListTasks.revalidate();
		this.jListTasks.repaint();
	}

