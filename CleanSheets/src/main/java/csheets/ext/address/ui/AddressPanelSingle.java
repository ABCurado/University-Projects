    private void jButtonEdit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEdit1ActionPerformed
		AddressPanelManager manager = new AddressPanelManager(this.controller, this.contact, this.address);
		int eventOption = JOptionPane.
			showConfirmDialog(null, manager, "Create/Edit Address", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
		if (eventOption == JOptionPane.OK_OPTION) {
			try {
				manager.createAddress();
			} catch (IOException ex) {
				Logger.getLogger(AddressPanelSingle.class.getName()).
					log(Level.SEVERE, null, ex);
			} catch (IllegalArgumentException ex) {
				JOptionPane.
					showMessageDialog(null, ex.getMessage(), "Create/Edit Address", JOptionPane.ERROR_MESSAGE);
			}
		}
    }//GEN-LAST:event_jButtonEdit1ActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
		int op = JOptionPane.
			showConfirmDialog(this, "Do you want to remove " + this.address.
							  getStreet() + " " + "?", "Remove Address", JOptionPane.OK_CANCEL_OPTION);

		if (JOptionPane.OK_OPTION == op) {
			if (jLabelTitle.getText().equalsIgnoreCase("Main Address")) {
				this.controller.removeAddress(this.contact, this.address, true);
			} else {
				this.controller.removeAddress(this.contact, this.address, false);
			}
		}
    }//GEN-LAST:event_jButtonDeleteActionPerformed

