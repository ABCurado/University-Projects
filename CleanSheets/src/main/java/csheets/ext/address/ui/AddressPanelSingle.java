	@Override
	public void update(Observable o, Object o1) {
		if (this.address != null) {
			if (this.contact.getMainAddress() != null) {
				if (this.contact.getMainAddress().equals(this.address)) {
					this.jLabelTitle.setText("Main Address");
				}
			}
			if (this.contact.getSecundaryAddress() != null) {
				if (this.contact.getSecundaryAddress().equals(this.address)) {
					this.jLabelTitle.setText("Secundary Address");
				}
			}
			this.jLabelCountry.setText(this.address.getCountry());
			this.jLabelCity.setText(this.address.getCity());
			this.jLabelStreet.setText(this.address.getStreet());
			this.jLabelPostalCode.setText(this.address.getPostalCode());
		}
	}
