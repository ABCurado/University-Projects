    private void jResultsListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jResultsListMouseClicked
		try {
			SearchResultDTO cell = (SearchResultDTO) jResultsList.
				getSelectedValue();
			String cellAddress = cell.getCell();
			int row = Integer.parseInt(cellAddress.substring(1)) - 1;
			int column = cellAddress.charAt(0) - 'A';

			if (!uiController.getActiveWorkbook().getParentFileName().
				equals(cell.getWorkBook())) {
				JOptionPane.
					showMessageDialog(this, "This cell is in a different workbook.");
				return;
			}

			if (!uiController.getActiveSpreadsheet().getTitle().trim().
				replaceAll("\\s+", " ").equals(cell.getSpreadSheet())) {
				JOptionPane.
					showMessageDialog(this, "This cell is in a different spreadsheet.");
				return;
			}

			uiController.focusOwner.changeSelection(row, column, false, false);
		} catch (NullPointerException ex) {
		}
    }//GEN-LAST:event_jResultsListMouseClicked

