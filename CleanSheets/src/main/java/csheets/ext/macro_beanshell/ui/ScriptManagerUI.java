    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
		String script = scriptList.getSelectedValue();
		if (script != null) {
			new CreateEditScriptUI(this, uiController, script).run();
		} else {
			message();
		}
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
		String script = scriptList.getSelectedValue();
		if (script != null) {
			int reply = JOptionPane.
				showConfirmDialog(this, "You want to eliminate this script?");
			if (reply == 0) {

				if (controller.deleteScript(script)) {
					JOptionPane.showMessageDialog(rootPane, "Eliminated");
				} else {
					JOptionPane.
						showMessageDialog(rootPane, "It's not possible to eliminate");
				}
			}
		} else {
			message();
		}
		displayScripts();
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void playButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playButtonActionPerformed
		String script = scriptList.getSelectedValue();

		if (script != null) {
			controller.executeScript(script);
		} else {
			message();
		}
    }//GEN-LAST:event_playButtonActionPerformed

