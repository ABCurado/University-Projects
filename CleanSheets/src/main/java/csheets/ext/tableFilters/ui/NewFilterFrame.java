	/**
	 * Adds a new filters to the table
	 *
	 * @throws IllegalValueTypeException
	 * @throws FormulaCompilationException
	 */
	private void addNewFilter() throws IllegalValueTypeException, FormulaCompilationException {
		int maxCol = this.uiController.getActiveSpreadsheet().getColumnCount() + 2;
		int maxRow = this.uiController.getActiveSpreadsheet().getRowCount() + 2;
		Cell tempCell = this.uiController.getActiveSpreadsheet().
			getCell(maxRow, maxRow);
		this.selectedTable.addFilter(this.jTextArea1.getText().trim(), tempCell);
	}
