	/**
	 * Retrieves the inserted Data.
	 */
	private void retrieveValues() {
		jAddButton.setEnabled(false); //false while this operations occurs.
		String _name = jNameField.getText();
		String _value = jValueField.getText();
		String _position = jPositionField.getText();
		if (_name == null || _name.isEmpty()) {
			showMessageDialog(null, "Please insert a valid Name", "Error", JOptionPane.ERROR_MESSAGE);
		} else if (_value == null || _value.isEmpty()) {
			showMessageDialog(null, "Please insert a valid Value", "Error", JOptionPane.ERROR_MESSAGE);
		} else {
			if (_position == null || _position.isEmpty() || _position.
				equals("0")) {
				_position = "1";
			}
			this.name = _name;
			this.value = _value;
			this.position = _position;
			dispose();
		}
		jAddButton.setEnabled(true);
	}

